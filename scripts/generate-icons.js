import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const missingIcons = [
  'car_game_icon.png',
  'ie_icon.png',
  'ie_large.png',
  'vhs-scanlines.png',
  '90s-pattern.png',
  'shopping/kitchen-set.png',
  'shopping/vacuum.png'
];

const publicDir = path.resolve(__dirname, '../public/lovable-uploads');

// Create a simple 1x1 pixel PNG
const pngData = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
  0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4, 0x89, 0x00, 0x00, 0x00,
  0x0A, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63, 0x00, 0x00, 0x00, 0x02,
  0x00, 0x01, 0x0D, 0x0D, 0x0D, 0x0D, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45,
  0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
]);

missingIcons.forEach(icon => {
  const iconPath = path.join(publicDir, icon);
  const iconDir = path.dirname(iconPath);
  
  if (!fs.existsSync(iconDir)) {
    fs.mkdirSync(iconDir, { recursive: true });
  }
  
  fs.writeFileSync(iconPath, pngData);
  console.log(`Created placeholder icon: ${icon}`);
}); 